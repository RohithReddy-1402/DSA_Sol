import turtle
t = turtle.Turtle()
t.fillcolor("red")
t.begin_fill()
t.forward(100)
t.right(60)
t.forward(100)
t.right(120)
t.forward(100)
t.right(60)
t.forward(200)
t.right(60)
t.forward(100)
t.right(120)
t.forward(100)
t.right(60)
t.forward(200)
t.right(60)
t.forward(100)
t.right(120)
t.forward(100)
t.right(60)
t.forward(100)
t.end_fill()

t.penup()
t.goto(-160,-200)
t.pendown()
