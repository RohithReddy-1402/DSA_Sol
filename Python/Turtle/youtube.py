import turtle
t=turtle.Turtle()
t.fillcolor("red")
t.begin_fill()
t.speed(100)
t.forward(100)
t.circle(25,90)
t.forward(112.5)
t.circle(25,90)
t.forward(200)
t.circle(25,90)
t.forward(112.5)
t.circle(25,90)
t.forward(100)
side=112.5//2
t.penup()
t.goto(-20,side+20)
t.right(90)
t.pendown()
t.forward(30)
t.left(120)
t.forward(60)
t.left(120)
t.forward(60)
t.left(120)
t.forward(30)
t.end_fill()
t.penup()
t.hideturtle()
turtle.done()